    public static void Consultar_Curso_Parametro(ObjectContainer base, String id) {

        Cursos Cbuscar = new Cursos(id, null, null, null, null);
        ObjectSet resul = base.get(Cbuscar);
        JOptionPane.showMessageDialog(null, "Existen " + resul.size() + " cursos");

    }

    public static int Validar_Curso(ObjectContainer BaseBD, String id) {

        Cursos Cbuscar = new Cursos(id, null, null, null, null);

        ObjectSet resul = BaseBD.get(Cbuscar);
        return resul.size();
    }

    public boolean validar_cursoc(ObjectContainer BaseBD, String id) {

        Cursos Vcurso = new Cursos(id, null, null, null, null);

        ObjectSet resul = BaseBD.get(Vcurso);

        validar = resul.hasNext();
        return validar;
    }

    public void mostrarTabla() {

        ObjectContainer BaseBD = Conexion.ConectarBD();
        Cursos Tcurso = new Cursos(null, null, null, null, null);
        ObjectSet resul = BaseBD.get(Tcurso);

        String matriz[][] = new String[resul.size()][5];

        for (int i = 0; i < resul.size(); i++) {
            Cursos cursoG = (Cursos) resul.next();
            matriz[i][0] = cursoG.getId_curso();
            matriz[i][1] = cursoG.getNombre_curso().toUpperCase();
            matriz[i][2] = cursoG.getFecha_ini().toUpperCase();
            matriz[i][3] = cursoG.getFecha_fin();
            matriz[i][4] = cursoG.getPrecio();
        }
        TablaRgtrcurso.setModel(new javax.swing.table.DefaultTableModel(
                matriz,
                new String[]{
                    "Id", "Nombre", "Fecha inicio", "Fecha fin", "precio"
                }
        ));
        BaseBD.close();
    }

