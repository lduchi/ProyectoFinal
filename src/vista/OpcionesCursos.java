    private void BtnRegistrocursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrocursoActionPerformed
        ObjectContainer BaseBD = Conexion.ConectarBD();
        if (txtidcurso.getText().equals("") || txtnombre.getText().equals("") || (fecha_ini.getDate().toString()).equals("") || (fecha_fina.getDate().toString()).equals("")
                || txtprecio.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
        } else {
            id = txtidcurso.getText().trim();
            nombre = txtnombre.getText().trim();
            fechaini = (fecha_ini.getDate().toString());
            fechafin = (fecha_fina.getDate().toString());
            precio = txtprecio.getText().trim();
            Cursos curso = new Cursos(id, nombre, fechaini, fechafin, precio);
            if (Validar_Curso(BaseBD, id) == 0) {
                if (vali.Validarcodigo(id)) {
                    if (vali.Validarnombrec(nombre)) {
                        if (vali.Validarprecio(precio)) {
                            BaseBD.set(curso);
                            JOptionPane.showMessageDialog(null, "El curso fue guardado exitosamente!");
                            limpiarLabel();
                        } else {
                            JOptionPane.showMessageDialog(null, "Ingrese el precio correcto, min 30 max 150 !!");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Nombre incorrecto ingrese nuevamente!");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "ID incorrecto ingrese nuevamente!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El cliente ya existe, no se puede registrar");
            }
            BaseBD.close();
            mostrarTabla();
        }
    }//GEN-LAST:event_BtnRegistrocursoActionPerformed

    private void btnModcursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModcursoActionPerformed
        ObjectContainer BaseBD = Conexion.ConectarBD();
        String idcurso = JOptionPane.showInputDialog("Ingrese el id del curso que desea Modificar: ");
        id = idcurso;
        Cursos Cbuscar = new Cursos(id, null, null, null, null);
        ObjectSet resul = BaseBD.get(Cbuscar);
        if (validar_cursoc(BaseBD, id)) {
            txtidcurso.setText(idcurso);
            txtidcurso.setEnabled(false);
            Cursos cursom = (Cursos) resul.next();
            String nomc = JOptionPane.showInputDialog("Ingrese el nombre del curso");
            txtnombre.setText(nomc.toUpperCase().trim());
            String fechain = JOptionPane.showInputDialog("Ingrese la fecha de inicio del curso");
            fecha_ini.getDateFormatString();
            String fechafin = JOptionPane.showInputDialog("Ingrese la fecha de fin del curso");
            fecha_fina.getDate().toString();
            String precioc = JOptionPane.showInputDialog("Ingrese el precio del curso");
            txtprecio.setText(precioc.toUpperCase().trim());

            cursom.setNombre_curso(nomc);
            cursom.setFecha_ini(fechain);
            cursom.setFecha_fin(fechafin);
            cursom.setPrecio(precioc);

            JOptionPane.showMessageDialog(null, "¡El curso ha sido modificado con exito!");
            BaseBD.set(cursom);
        } else {
            JOptionPane.showMessageDialog(null, "¡El curso no está registrado en la base de datos!");
        }
        txtidcurso.setEnabled(true);
        limpiarLabel();
        BaseBD.close();
        mostrarTabla();

    }//GEN-LAST:event_btnModcursoActionPerformed

